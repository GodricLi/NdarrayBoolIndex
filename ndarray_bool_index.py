import numpy as np

"""布尔索引"""
"""
问题1：给一个数组，选出数组中所有大于5的数
"""
a = np.array([i for i in range(10)])
print(a[a > 5])

"""
问题2：给一个数组，选出数组中所有大于5的偶数
"""
# 多个条件同时满足，and
print(a[(a > 5) & (a % 2 == 0)])

"""
问题3：给一个数组，选出数组中所有大于5的数和偶数
"""
# 多个条件满足其一，or
print(a[(a > 5) | (a % 2 == 0)])
